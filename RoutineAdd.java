    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String time1, time2;
        time1 = jTextField1.getText();
        time2 = jTextField2.getText();
    
        try {
            String sql = "INSERT INTO routine(day, time, chapter) VALUES(?,?,?)";
            PreparedStatement prstmnt = connect.connection.prepareStatement(sql);
            prstmnt.setString(1, day);
            prstmnt.setString(2, time1 + "-" + time2);
            prstmnt.setString(3, chapter);

            prstmnt.executeUpdate();
            prstmnt.close();

            int input = JOptionPane.showOptionDialog(null, "Added Successfully!", "The title",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE, null, null, null);
            if (input == JOptionPane.OK_OPTION) {
                super.dispose();
            }

        } catch (SQLException | NumberFormatException | HeadlessException ex) {
            System.out.println(ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

